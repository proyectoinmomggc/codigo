    void selecciontabla() throws Exception {
        p_fichainquilino fic = null;
        p_control con = p_control.getInstancia();
        
        int row = tblinquilinos.getSelectedRow(); //guarda el numero de fila que selecciona el usuario

        if (row != -1) {
            d_inquilino inq = new d_inquilino();
            inq.setProp_id((Integer) tblinquilinos.getValueAt(row, 0));
            inq.setInq_casa((Integer) tblinquilinos.getValueAt(row, 1));
            inq.setInq_nombre((String) tblinquilinos.getValueAt(row, 2));
            inq.setInq_direccion((String) tblinquilinos.getValueAt(row, 3));
            inq.setInq_tel((Integer) tblinquilinos.getValueAt(row, 4));
            inq.setInq_observaciones((String) tblinquilinos.getValueAt(row, 5));
            inq = inq.buscarinquilino(inq.getProp_id(), inq.getInq_casa());
            con.inq = inq;
            fic = new p_fichainquilino(new JDialog(), true);
            fic.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, toUpperCase("debe seleccionar una fila"), "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }

