    void funcbuscar() {
        d_inquilino inq = new d_inquilino();
        d_inquilino inq1 = null;
        Vector v;
        
        try {
            if (cmbordenar.getSelectedItem().equals("ID")) {
                listainquilinos = inq.listarinquilinos();
            }
            if (cmbordenar.getSelectedItem().equals("NOMBRE")) {
                listainquilinos = inq.listarinquilinospornombre();
            }
            if (cmbordenar.getSelectedItem().equals("DIRECCION")) {
                listainquilinos = inq.listarinquilinospordireccion();
            }
            cleartable();
            for (d_inquilino aux : listainquilinos) {
                
                inq1 = new d_inquilino();
                v = new Vector();
                inq1.setProp_id(aux.getProp_id());
                inq1.setInq_casa(aux.getInq_casa());
                inq1.setInq_nombre(aux.getInq_nombre());
                inq1.setInq_direccion(aux.getInq_direccion());
                inq1.setInq_tel(aux.getInq_tel());
                inq1.setInq_observaciones(aux.getInq_observaciones());
                v.add(inq1.getProp_id());
                v.add(inq1.getInq_casa());
                v.add(inq1.getInq_nombre());
                v.add(inq1.getInq_direccion());
                v.add(inq1.getInq_tel());
                v.add(inq1.getInq_observaciones());
                mdltabinq.addRow(v);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, toUpperCase(ex.getMessage()), "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }

