    void cargardatoscaja() {
        p_control con = p_control.getInstancia();
        d_caja caja = null;
        d_caja aux = new d_caja();
        Float cajainicial = 0f;
        Float entrada = 0f;
        Float salida = 0f;
        Float total = 0f;

        try {
            cajainicial = 0f;
            caja = aux.buscarcaja();
            if (caja != null) {
                cajainicial = caja.getMonto();
            }
            entrada = totalentradatipopagohoy();
            salida = totalsalidatipopagohoy();
            txtcajainicial.setText(con.mostrarnumero(cajainicial));
            txtentrada.setText(con.mostrarnumero(entrada));
            txtsalida.setText(con.mostrarnumero(salida));
            txtvalor.setText("0");
            total = devuelvemontototal();
            txttotal.setText(con.mostrarnumero(total));
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, toUpperCase(ex.getMessage()), "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }

    void cargardatoscajabdd() {
        p_control con = p_control.getInstancia();
        d_caja caja = null;
        d_caja aux = new d_caja();
        float sindatos = 0f;

        try {
            caja = aux.buscarcaja();
            if (caja == null) {
                lbldatoscaja.setText(con.mostrarnumero(sindatos) + " CAJA NO INICIADA");
            } else {
                String fecha = parsefechastring(caja.getFecha());
                lbldatoscaja.setText("$: " + con.mostrarnumero(caja.getMonto()) + " " + "FECHA: " + fecha);
                txtcajainicial.setText(con.mostrarnumero(caja.getMonto()));
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, toUpperCase(ex.getMessage()), "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }

