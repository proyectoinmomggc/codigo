    float devuelvemontototal() throws Exception {
        p_control con = p_control.getInstancia();
        float cajainicial = 0f;
        float valor = 0f;
        //float montototal = 0f;

        if (txtcajainicial.getText().isEmpty()) {
            cajainicial = 0f;
        } else {
            cajainicial = con.guardarnumero((txtcajainicial.getText()));
        }
        
        if (txtvalor.getText().isEmpty()) {
            valor = 0f;
        } else {
            valor = con.guardarnumero((txtvalor.getText()));
        }
        montototal = (cajainicial+(valor)
                + con.guardarnumero((txtentrada.getText())))
                - (con.guardarnumero((txtsalida.getText())));
        return montototal;
    }

